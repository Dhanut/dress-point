package shopping;

public class Cloth {
    private String clothName;
    private String clothSize;
    private String clothImagePath;
    private double clothPrice;
    private String clothColor;
    private String clothMaterial;


    public String getClothName() {
        return clothName;
    }

    public void setClothName(String clothName) {
        this.clothName = clothName;
    }

    public String getClothSize() {
        return clothSize;
    }

    public void setClothSize(String clothSize) {
        this.clothSize = clothSize;
    }

    public String getClothImagePath() {
        return clothImagePath;
    }

    public void setClothImagePath(String clothImagePath) {
        this.clothImagePath = clothImagePath;
    }

    public double getClothPrice() {
        return clothPrice;
    }

    public void setClothPrice(double clothPrice) {
        this.clothPrice = clothPrice;
    }

    public String getClothColor() {
        return clothColor;
    }

    public void setClothColor(String clothColor) {
        this.clothColor = clothColor;
    }

    public String getClothMaterial() {
        return clothMaterial;
    }

    public void setClothMaterial(String clothMaterial) {
        this.clothMaterial = clothMaterial;
    }
}
